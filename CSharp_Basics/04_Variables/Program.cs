 A variable is nothing but a name given to a storage area that our programs can manipulate.
 Each variable in C# has a specific type, which determines the size and layout of the variable's memory the range of values
 that can be stored within that memory and the set of operations that can be applied to the variable.
 
     The basic value types provided in C# can be categorized as −

        Type	Example
        Integral types  =>	        sbyte, byte, short, ushort, int, uint, long, ulong, and char
        Floating point types ==>	float and double
        Decimal types       ==> 	decimal
        Boolean types	==>         true or false values, as assigned
        Nullable types	==>          Nullable data types


        C# also allows defining other value types of variable such as <enum> and reference types of variables such as <class>


        Defining Variables
        <data_type> <variable_list>;
        Here, data_type must be a valid C# data type 
        including char, int, float, double, or any user-defined data type, and variable_list
        may consist of one or more identifier names separated by commas.

        Initializing Variables
        variables are initialized (assigned a value) with an equal sign followed by a constant expression. The general form of initialization is −

        variable_name = value;
        Variables can be initialized in their declaration. The initializer consists of an equal sign followed by a constant expression as −

        <data_type> <variable_name> = value;
